package exercise2;

import java.util.Scanner;

public class Circle {

    public static void main(String[] args) {
      Scanner input = new Scanner(System.in);

     double Pi = 3.14;
     double r = input.nextDouble();
     float C = 2*Pi*r;
     float A = Pi*r*r;

     System.out.println("Circumference: " + C);
     System.out.println("Area: " + A );
    

      






     }
    }
